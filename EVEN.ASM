ASSUME CS:CODE,DS:DATA
DATA SEGMENT
M1 DB 0AH,0DH,"ENTER A NUMBER$"
M2 DB 0AH,0DH,"THE NUMBER IS EVEN $"
M3 DB 0AH,0DH,"THE NUMBER IS ODD $" 
DATA ENDS
CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

LEA DX,M1
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
MOV BL,AL
ADD BL,48
TEST BL,01

JNE ODD1 

LEA DX,M2
MOV AH,09H
INT 21H
JE EXIT
ODD1:
        LEA DX,M3
        MOV AH,09H
        INT 21H
        JE EXIT
EXIT:
        MOV AH,4CH
        INT 21H
CODE ENDS
END START
        
